def sampling(N,seed,std,l_mean,r_mean):   
    random.seed(seed)
    rd = random.randn(N)
    RSSI = np.r_[std*rd[:N/2]+l_mean, std*rd[N/2:]+r_mean]
    random.seed() 
    return RSSI
    
